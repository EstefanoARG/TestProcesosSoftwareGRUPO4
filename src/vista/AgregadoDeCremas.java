    private void agregarSalsa(String salsa) {
        if (detallePedidoActual != null) {
            detallePedidoActual.agregarSalsa(salsa);
            salsasAgregadas++;
            if (salsasAgregadas > 8) {
                double nuevoPrecio = detallePedidoActual.getProducto().getPrecio() + COSTO_SALSA_EXTRA;
                detallePedidoActual.getProducto().setPrecio(nuevoPrecio);
                jPrecio.setText(String.valueOf(nuevoPrecio));
            }
            actualizarDetalleSalsas();
        }
    }

    private void actualizarDetalleSalsas() {
        if (detallePedidoActual != null) {
            System.out.println("Salsas añadidas: " + detallePedidoActual.getSalsas());
        }
    }
