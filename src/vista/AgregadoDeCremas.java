    private void verTicketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_verTicketMouseClicked
    // Verificar si el pedido tiene detalles antes de mostrar el ticket
    Pedido pedidoActual = ControladorPedido.getPedidoActual();
    pedidoActual.agregarDetalle(detallePedidoActual);
    ControladorPedido.guardarPedido();

    if (pedidoActual != null && pedidoActual.getDetalles().size() > 0) {
        VerTicket vistaTicket = new VerTicket(pedidoActual);
        vistaTicket.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "No hay detalles de pedido para mostrar.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_verTicketMouseClicked

